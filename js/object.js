//object data type. defined by curly braquets
//every object has a key and one value

var obj = {}; //empty object
var car = {
    make: 'honda', //key=make  //value=honda
    model: 'civic',
    year: 2019
};
console.log(car); //gives us everything; keys and values
console.log(car.model); //gives us only the model type
console.log(car.year); //gives us only the year

var date = {
    date: 23,
    month: 'April',
    year: 2019,
    day: 'Tuesday'
};
console.log(date.day);

